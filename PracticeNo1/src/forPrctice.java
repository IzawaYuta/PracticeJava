public class forPrctice {
    public static void main(String[] args) {

        //基本的な繰り返し（インクリメント）
        int num = 0;
        for(num = 1; num <= 10; num++) {
            System.out.println(num);
        }

        //デクリメント
        int value = 5;
        for(value = 5; value >= 0; value--) {
            System.out.println(value);
        }
    }
}
